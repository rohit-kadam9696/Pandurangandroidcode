package com.twd.chitboyapp.spsskl.pojo;

public class PlantLocation {

    private String nplotNoOffline;
    private String vyearId;
    private double vlatitude;
    private double vlongitude;
    private double naccuracy;

    public String getNplotNoOffline() {
        return nplotNoOffline;
    }

    public void setNplotNoOffline(String nplotNoOffline) {
        this.nplotNoOffline = nplotNoOffline;
    }

    public String getVyearId() {
        return vyearId;
    }

    public void setVyearId(String vyearId) {
        this.vyearId = vyearId;
    }

    public double getVlatitude() {
        return vlatitude;
    }

    public void setVlatitude(double vlatitude) {
        this.vlatitude = vlatitude;
    }

    public double getVlongitude() {
        return vlongitude;
    }

    public void setVlongitude(double vlongitude) {
        this.vlongitude = vlongitude;
    }

    public double getNaccuracy() {
        return naccuracy;
    }

    public void setNaccuracy(double naccuracy) {
        this.naccuracy = naccuracy;
    }
}
