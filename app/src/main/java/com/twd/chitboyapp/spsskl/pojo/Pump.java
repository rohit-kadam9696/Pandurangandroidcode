package com.twd.chitboyapp.spsskl.pojo;

public class Pump {
    private int npumpId;
    private String vpumpName;
    private int nitemId;
    private String vitemName;

    public int getNpumpId() {
        return npumpId;
    }

    public void setNpumpId(int npumpId) {
        this.npumpId = npumpId;
    }

    public String getVpumpName() {
        return vpumpName;
    }

    public void setVpumpName(String vpumpName) {
        this.vpumpName = vpumpName;
    }

    public int getNitemId() {
        return nitemId;
    }

    public void setNitemId(int nitemId) {
        this.nitemId = nitemId;
    }

    public String getVitemName() {
        return vitemName;
    }

    public void setVitemName(String vitemName) {
        this.vitemName = vitemName;
    }
}
